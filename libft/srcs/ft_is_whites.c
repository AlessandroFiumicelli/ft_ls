#include "../includes/libft.h"

int		ft_is_whites(char const *s)
{
	int len;

	len = 0;
	while (s[len])
	{
		if (s[len] != ' ' && s[len] != '\t' && s[len] != '\n')
			return (0);
		len++;
	}
	return (1);
}
